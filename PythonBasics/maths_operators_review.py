# Comments and Math Operators Review Exercises

# 1. Create a variable and assign it the sum of two integers
sum_result = 10 + 5  # 10 plus 5 equals 15

# 2. Create a variable and assign it the difference of two integers
difference_result = 20 - 7  # 20 minus 7 equals 13

# 3. Create a variable and assign it the result of one integer being divided by another integer
division_result = 16 / 4  # 16 divided by 4 equals 4.0 (float)

# 4. Create a variable and assign it the product of two integers
product_result = 6 * 7  # 6 times 7 equals 42

# 5. Create a variable and assign it the result of 3 raised to the 8th power
power_result = 3 ** 8  # 3 to the power of 8 equals 6561

# 6. Create a variable and assign it the result of 10 floor division 3
floor_div_result = 10 // 3  # Floor division of 10 by 3 equals 3

# 7. Create a variable and assign it the integer 2 using the expression 17 modulo [number]
modulo_result = 17 % 5  # 17 modulo 5 equals 2

# Printing all results for verification
print("Sum:", sum_result)
print("Difference:", difference_result)
print("Division:", division_result)
print("Product:", product_result)
print("Power (3^8):", power_result)
print("Floor Division (10 // 3):", floor_div_result)
print("Modulo (17 % 5):", modulo_result)
